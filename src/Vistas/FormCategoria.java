    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        if (ValidarCamposVacio()) {
            JOptionPane.showMessageDialog(this, "Existe un campo vacio");
        } else {
            int codigo = Integer.parseInt(txtcodigo.getText());
            Categorias ctg = cp.findCategorias(codigo);
            if (ctg == null) {
                String nombre = txtnombre.getText();
                Categorias ct = new Categorias();
                ct.setCodigo(codigo);
                ct.setNombre(nombre);
                try {
                    cp.create(ct);
                    JOptionPane.showMessageDialog(this, "Registro guardado correctamente");
                    AgregarfilaTabla(codigo, nombre);
                    LimpiarFormulario();

                } catch (PreexistingEntityException ex) {
                    Logger.getLogger(FormCategoria.class.getName()).log(Level.SEVERE, null, ex);
                } catch (Exception ex) {
                    Logger.getLogger(FormCategoria.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Codigo: " + codigo + " ya existe");
            }
        }
    }//GEN-LAST:event_guardarActionPerformed

