    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String buscarProducto = txtproductos.getText().toLowerCase();

        // Limpiar la tabla antes de aplicar el filtro
        DefaultTableModel modelo = (DefaultTableModel) tabla2.getModel();
        modelo.setRowCount(0);

        // Recorrer los datos originales y agregar filas que cumplan con el criterio de b√∫squeda
        List<Productos> lp = pro.findProductosEntities();
        boolean ProductoEncontrado = false;

        for (Productos productos : lp) {
            if (String.valueOf(productos.getCodigoProducto()).contains(buscarProducto)) {
                // Agrega la fila a la tabla
                Object fila[] = {productos.getCodigoProducto(), productos.getNombreProducto(), productos.getProveedoresCodigoRut(), productos.getUnidadMedida(), productos.getPrecioUnitario(), productos.getCantidadDisponible()};
                modelo.addRow(fila);
                ProductoEncontrado = true;
            }
        }

        if (!ProductoEncontrado) {
            JOptionPane.showMessageDialog(this, "No se encontr√≥ ning√∫n producto con ese nombre.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String buscarNumeroFactura = txtfactura.getText().toLowerCase();

        DefaultTableModel modelo = (DefaultTableModel) Table3.getModel();
        modelo.setRowCount(0);

        List<Factura> lp = factura.findFacturaEntities();
        boolean facturaEncontrada = false;

        for (Factura factura : lp) {
            if (String.valueOf(factura.getCodigo()).contains(buscarNumeroFactura)) {
                // Agrega la fila a la tabla
                Object fila[] = {factura.getCodigo(), factura.getFecha(), factura.getUsuariosCedula(), factura.getClientesCedula()};
                modelo.addRow(fila);
                facturaEncontrada = true;
            }
        }

        if (!facturaEncontrada) {
            JOptionPane.showMessageDialog(this, "No se encontr√≥ una factura con el n√∫mero proporcionado.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

