    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (ValidarCamposVacio()) {
            JOptionPane.showMessageDialog(this, "Existe un campo vacio");
        } else {
            
            int codigo = Integer.parseInt(txtcodigo.getText());
            int cantidad = Integer.parseInt(txtcantidad.getText());
            Productos productos1 = (Productos) com1.getSelectedItem();
            Factura facturas1 = (Factura) com2.getSelectedItem();

            Productoventa venta = new Productoventa();
            venta.setCodigo(codigo);
            venta.setCantidad(cantidad);
            venta.setProductosCodigoProducto(productos1);
            venta.setFacturacodigo(facturas1);

            try {
                vent.create(venta);
                JOptionPane.showMessageDialog(this, "Registro guardado");
                CargarTablaVentas();
                
            } catch (Exception e) {
                Logger.getLogger(FromVentas.class.getName()).log(Level.SEVERE, null, e);
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

