    public boolean ValidarCamposVacio() {
        if (txtcodigo.getText().equals("")) {
            txtcodigo.requestFocus();
            return true;
        }
        if (txtcantidad.getText().equals("")) {
            txtcantidad.requestFocus();
            return true;
        }
        return false;
    }
    
    public void LimpiarFormulario() {
        txtcodigo.setText("");
        txtcantidad.setText("");
    }

