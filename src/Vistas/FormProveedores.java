    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        if(ValidarCamposVacio())
            {
                JOptionPane.showMessageDialog(this, "Existe un campo vacio");
            }
            else
            {
        int codigo=Integer.parseInt(txtcodigo.getText());
        Proveedores prov=pc.findProveedores(codigo);
        if (prov==null) {
           String nombre=txtnombre.getText();
           String direccion=txtdireccion.getText();
           int tel1=Integer.parseInt(txttel1.getText());
           int tel2=Integer.parseInt(txttel2.getText());
           Proveedores pv=new Proveedores();
           pv.setCodigoRut(codigo);
           pv.setNombreRazonSocial(nombre);
           pv.setDireccion(direccion);
           pv.setTelefono1(tel1);
           pv.setTelefono2(tel2);
           try
                  { 
                  pc.create(pv);
                  JOptionPane.showMessageDialog(this, "Registro guardado correctamente");
                  AgregarfilaTabla(codigo,nombre,direccion,tel1,tel2);
                  LimpiarFormulario();
                  
                 }catch (PreexistingEntityException ex) {
                   Logger.getLogger(FormCategoria.class.getName()).log(Level.SEVERE, null, ex);
                 } catch (Exception ex) {
                   Logger.getLogger(FormCategoria.class.getName()).log(Level.SEVERE, null, ex);
                 }
        }else
         {
            JOptionPane.showMessageDialog(this, "Codigo: "+codigo+" ya existe");
         }
            }
    }//GEN-LAST:event_guardarActionPerformed

    private void btnActualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizar1ActionPerformed
          int respuesta = JOptionPane.showConfirmDialog(this, "¬øEst√° seguro de eliminar este registro?");
        if (respuesta == JOptionPane.YES_OPTION) {
            int codigo = Integer.parseInt(txtcodigo.getText());
            try {
                // Supongamos que factura.destroy(codigo) es la forma correcta de eliminar un registro.
                pc.destroy(codigo);
                JOptionPane.showMessageDialog(this, "Registro eliminado correctamente.");
                LimpiarFormulario(); // Limpia el formulario despu√©s de la eliminaci√≥n.
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al eliminar el registro: " + e.getMessage());
            }
        }
        CargarTabla();
    }//GEN-LAST:event_btnActualizar1ActionPerformed

    public void AgregarfilaTabla(int codigo, String nombre, String direccion, int tel1, int tel2)
    {
        Object fila[]={codigo,nombre,direccion,tel1,tel2};
        DefaultTableModel modelo=(DefaultTableModel) tabla.getModel();
        modelo.addRow(fila);
    }
    public void LimpiarFormulario()
    {
        txtcodigo.setText(null);
        txtnombre.setText(null);
        txtdireccion.setText(null);
        txttel1.setText(null);
        txttel2.setText(null);
        txtcodigo.requestFocus();
    }
    public void CargarTabla()
    {
        DefaultTableModel modelo=(DefaultTableModel) tabla.getModel();
        List<Proveedores> lp=pc.findProveedoresEntities();
        modelo.setRowCount(0);
        for (int i = 0; i < lp.size(); i++) {
           Object fila[]={lp.get(i).getCodigoRut(),lp.get(i).getNombreRazonSocial(),lp.get(i).getDireccion(),lp.get(i).getTelefono1(),lp.get(i).getTelefono2()}; 
           modelo.addRow(fila);
        }
    }
    public boolean ValidarCamposVacio()
    {
      if(txtcodigo.getText().equals(""))
      {
          txtcodigo.requestFocus();
          return true;
      }
      if(txtnombre.getText().equals(""))
      {
          txtnombre.requestFocus();
          return true;
      }
      if(txtdireccion.getText().equals(""))
      {
          txtdireccion.requestFocus();
          return true;
      }
      if(txttel1.getText().equals(""))
      {
          txttel1.requestFocus();
          return true;
      }
      if(txttel2.getText().equals(""))
      {
          txttel2.requestFocus();
          return true;
      }
      return false;
    }
    
    private void SetImagenLabel(JLabel labelName, String root) {
        ImageIcon image = new ImageIcon(root);
        Icon icon = new ImageIcon(image.getImage().getScaledInstance(labelName.getWidth(), labelName.getHeight(), Image.SCALE_DEFAULT));
        labelName.setIcon(icon);
        this.repaint();
    }

